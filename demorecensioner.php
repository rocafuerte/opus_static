Trots att det rör sig om två trior med sjungande gitarrister står 
basisterna högt i kurs. I kanadensiska Ken Modes läger huserar 
rödtotten Chad Tremblay, som pillar fram så medryckande slingor att 
man undrar om han fuskar som flöjtande ormtjusare för att få stålar 
till hyran. I symbios med sin sexsträngade kollega Jesse Matthewson 
stampar han omkring och rycker rytmiskt som om de tillsammans vore i 
full färd att frammana något stort och elakt.<br />
<br />

Herrarnas rituella beteende är oerhört kul att iaktta och utgör en 
cool kontrast mot det kaotiska i musiken som främst härstammar från 
det tokdistade gitarrspelet samt oväntade manövrar bakom trumsetet. 
Tyngd och hastighet prioriteras lika högt i ken modes 
hardcorebaserade, svängiga vansinne. Att bandet refererar till 
melvins gällande inspiration är självklart, att Jesse kombinerar 
cowboyhatt med ett David Bowie-sminkat öga är mindre givet men 
onekligen stiligt. [7]<br />
<br />

Ingen av medlemmarna i walesiska Taint vågar sig på en fullt så modig 
look, men de har en hel del skägg, vilket som vi alla vet är värt en 
hel del. Särskilt när man häver ur sig tjockbottnat ös i gränslandet 
mellan sludge metal och stonerrock. Manskapet visar upp den rockigare 
sidan och lämnar det värsta oväsendet i resväskan. Lite synd kan 
tyckas, då det är de vildaste låtarna som särskiljer Taint från 
mängden. Några problem med att behålla publikens uppmärksamhet har 
trion omsider inte. Öronen hålls öppna, byxorna fladdrar och 
mustaschfjunet vibrerar av de maxade förstärkarna.<br />
<br />

Den vältatuerade Chris West genererar de starkaste vibrationerna med 
sina fem tjocka strängar och sätter sista spiken i kistan för myten 
om att basister i rockband bara märks av i sin frånvaro. Att höra 
människan smiska upp sitt instrument är så pass underhållande att 
frånvaron av esset ”Triumverate” från fjolårsalbumet inte gör något. [6]
David Jannati.
